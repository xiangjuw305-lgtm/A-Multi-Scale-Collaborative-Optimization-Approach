"""
train_and_evaluate_ours.py

Main reproduction script for the proposed method reported in Table 2 and Table 3 of the manuscript:
"A Multi-Scale Collaborative Optimization Approach for Infrared Target Detection of Electrical Apparatus in Switchgear Cabinets".

This script corresponds to the final YOLOv10m-based model with:
1. Target Aug: minority-class-oriented training-list resampling/augmentation;
2. Class Weight: class-frequency-aware classification loss weighting;
3. SGRB-DE: training-stage semantic-guided ROI-background decoupled enhancement;
4. C2f-SimAM: parameter-free attention inserted into the P3/P4 neck feature aggregation blocks;
5. MPDIoU: point-distance-penalized bounding-box regression loss.

Important implementation notes:
- SGRB-DE enhanced samples are used only in the training list. The validation split remains the original raw validation set.
- No ground-truth boxes, semantic masks, or manually specified ROI information are used during validation or inference.
- SimAM is parameter-free; MPDIoU changes the training loss only and does not increase inference-time parameters or GFLOPs.
- The script assumes that the corresponding C2f-SimAM and MPDIoU components have been registered in the local YOLOv10/Ultralytics environment.

Before running:
1. Install the same YOLOv10/Ultralytics environment used in the manuscript.
2. Register the C2f-SimAM module and the MPDIoU loss in the local implementation.
3. Update the paths in the USER CONFIG section below.

Example:
python train_and_evaluate_ours.py ours
python train_and_evaluate_ours.py m4_without_mpdiou
"""

